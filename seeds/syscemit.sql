CREATE TABLE graves (
	id INTEGER NOT NULL, 
	street VARCHAR(5) NOT NULL, 
	number VARCHAR(5) NOT NULL, 
	zone_id INTEGER, 
	PRIMARY KEY (id), 
	FOREIGN KEY(zone_id) REFERENCES zones (id)
);
CREATE TABLE districts (
	id INTEGER NOT NULL, 
	name VARCHAR(255) NOT NULL, 
	city_id INTEGER, 
	PRIMARY KEY (id), 
	FOREIGN KEY(city_id) REFERENCES cities (id)
);
CREATE TABLE registries (
	id INTEGER NOT NULL, 
	name VARCHAR(255) NOT NULL, 
	city_id INTEGER, 
	PRIMARY KEY (id), 
	FOREIGN KEY(city_id) REFERENCES cities (id)
);
CREATE TABLE streets (
	id INTEGER NOT NULL, 
	name VARCHAR(255) NOT NULL, 
	cep VARCHAR(8) NOT NULL, 
	district_id INTEGER, 
	PRIMARY KEY (id), 
	FOREIGN KEY(district_id) REFERENCES districts (id)
);
CREATE TABLE addresses (
	id INTEGER NOT NULL, 
	number VARCHAR(5), 
	complement VARCHAR(255), 
	street_id INTEGER, 
	PRIMARY KEY (id), 
	FOREIGN KEY(street_id) REFERENCES streets (id)
);
CREATE TABLE deceased (
	id INTEGER NOT NULL, 
	name VARCHAR(255) NOT NULL, 
	age INTEGER NOT NULL, 
	birth_date DATE NOT NULL, 
	gender BOOLEAN NOT NULL, 
	birthplace_id INTEGER, 
	ethnicity_id INTEGER, 
	civil_state_id INTEGER, 
	home_address_id INTEGER, 
	grave_id INTEGER, 
	PRIMARY KEY (id), 
	CHECK (gender IN (0, 1)), 
	FOREIGN KEY(birthplace_id) REFERENCES cities (id), 
	FOREIGN KEY(ethnicity_id) REFERENCES ethnicities (id), 
	FOREIGN KEY(civil_state_id) REFERENCES civil_states (id), 
	FOREIGN KEY(home_address_id) REFERENCES addresses (id), 
	FOREIGN KEY(grave_id) REFERENCES graves (id)
);
CREATE TABLE memberships (
	children_id INTEGER NOT NULL, 
	deceased_id INTEGER NOT NULL, 
	PRIMARY KEY (children_id, deceased_id), 
	FOREIGN KEY(children_id) REFERENCES childrens (id), 
	FOREIGN KEY(deceased_id) REFERENCES deceased (id)
);
CREATE TABLE deaths (
	id INTEGER NOT NULL, 
	registration VARCHAR(40) NOT NULL, 
	datetime DATETIME NOT NULL, 
	cause VARCHAR(1500) NOT NULL, 
	address_id INTEGER, 
	deceased_id INTEGER, 
	registry_id INTEGER, 
	doctor_id INTEGER, 
	PRIMARY KEY (id), 
	FOREIGN KEY(address_id) REFERENCES addresses (id), 
	FOREIGN KEY(deceased_id) REFERENCES deceased (id), 
	FOREIGN KEY(registry_id) REFERENCES registries (id), 
	FOREIGN KEY(doctor_id) REFERENCES doctors (id)
);
